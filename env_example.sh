# Instructions for getting API keys:
# 1. v0.dev API Key: 
#    - Go to https://v0.dev/api or https://v0.dev/settings
#    - Sign up/login with your GitHub account
#    - Navigate to API section
#    - Generate a new API key
#    - Copy the key to V0_API_KEY above
#
# 2. Email Setup: Use Gmail app password for EMAIL_PASS (not your regular Gmail password)
#    - Enable 2FA on your Gmail account
#    - Go to Google Account settings > Security > App passwords
#    - Generate an app password for this application
#
# Note: v0.dev API is currently in beta - check their documentation for latest endpoints